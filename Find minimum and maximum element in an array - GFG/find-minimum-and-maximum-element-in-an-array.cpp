pair<long long, long long> getMinMax(long long a[], int n) {
    long long int ans1 = INT_MAX;
    long long int ans2 = INT_MIN;
    for(int i=0;i<n;i++){
        ans1 = min(a[i],ans1);
        ans2 = max(a[i],ans2);
    }
   return {ans1,ans2};
}