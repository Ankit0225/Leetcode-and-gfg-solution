class Solution
{
    public:
        int maxOnes (vector <vector <int>> &Mat, int N, int M)
        {
            // your code here
            int rowNo=-1,row=0,col=M-1;
            while(row<N && col>=0)
            {
                if(Mat[row][col]==1)
                {
                    rowNo=row;
                    col--;
                }
                else
                row++;
            }
            return rowNo;
        }
};

