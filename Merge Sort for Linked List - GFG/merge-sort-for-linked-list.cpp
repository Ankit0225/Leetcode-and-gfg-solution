class Solution{
  public:
    //Function to sort the given linked list using Merge Sort.
    Node* mergeSort(Node* head) {
        // your code here
        Node *curr=head;
        vector<int> ans;
        while(curr!=NULL)
        {
            ans.push_back(curr->data);
            curr=curr->next;
        }
        sort(ans.begin(),ans.end());
        Node* head1=NULL;
        Node* tail=NULL;
        for(int i=0;i<ans.size();i++)
        {
            if(head1==NULL)
            {
                head1=tail= new Node(ans[i]);
            }
            else
            {
                tail->next= new Node(ans[i]);
                tail=tail->next;
            }
        }
        tail->next=NULL;
        return head1;
    }
};


